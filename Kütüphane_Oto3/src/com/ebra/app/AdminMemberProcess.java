    private void btnUpdateMemberActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateMemberActionPerformed
         int userId = Integer.parseInt(tbId.getText());
         String name = tbName.getText();
         String surname = tbSurname.getText();
         String username = tbUsername.getText();
         String password = new String(tbPassword.getPassword());
         String role = (String) cbRole.getSelectedItem();
         
         UserDto userDto=new UserDto();
     
       
         userDto.setId(userId);
         userDto.setName(name);
         userDto.setSurname(surname);
        
         userDto.setUser(username);
         
            if(!password.equals("")){
            userDto.setPassword(password);
            
        }
         RoleDto roleDto = new RoleDto();
         roleDto.setRoleName(role);
         
         
         int id = roleDao.getId(roleDto);
         
            if(id != 0) userDto.setRoleId(id);
         
         userDao.updateUser(userDto);
         userDao.editRole(userDto);
    }//GEN-LAST:event_btnUpdateMemberActionPerformed

