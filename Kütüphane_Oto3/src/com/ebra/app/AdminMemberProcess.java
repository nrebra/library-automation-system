    public void getRoles(){
       ArrayList<RoleDto> roles = new ArrayList<RoleDto>();
       
       roles = roleDao.getRoles();
       if(roles != null){
            for(RoleDto role : roles){
              cbRole.addItem(role.getRoleName());
              
            }
        }
    }
    public void showUsers(){
        model.setRowCount(0);
        ArrayList<UserDto> users = new ArrayList<UserDto>();
        users = userDao.getUsers();
        if(users != null){
            for(UserDto person : users){
                Object[] prsn = {person.getId(),person.getName(),person.getSurname(),person.getUser(),person.getRoleName()};
                model.addRow(prsn);
            }
        }
    }
